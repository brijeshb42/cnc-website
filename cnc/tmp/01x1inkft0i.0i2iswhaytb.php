Game Version
Counter Strike 1.6 (Non-Steam)
ESRB Rating: Age 17+
General
All teams will play against each other and then top 2 from each group shall proceed to the next round
Map elimination will take place before every match by coin toss
Competition Method
Official Maps: De_Dust2, De_Inferno, De_Nuke, De_Train.
5 vs 5 (Team Play, 5 players per team)
The 5 members of the team will be fixed and wont be changed under any circumstances.
Victory Condition: The first team to win 16 rounds is declared the winner
If both the teams do not agree for sides (in terms of systems) a coin toss will decide for the sides. A coin toss will determine starting sides, Counter-Terrorist or Terrorist
Semis n Finals will be best of three with 1 map eliminated by coin toss, and in case there is shortage of time semis will be of 1 map.
In the case of a 15-15 tie after regulation, the following steps will be taken:
a) If the match is in first phase, then the match will remain a tie and each team awarded 1 point.
If the match is in second phase, then the match will have overtime
Overtime consists of 6 rounds with starting money of $10,000 Each team will play 3 rounds as Terrorist and 3 rounds as Counter-Terrorist The team to win 4 rounds first, is declared the winner In the case of another tie, the overtime will be repeated until a winner is declared
Only team leaders are allowed to use public in-game messages (messagemode1) within a match The rule also applies when dead Use of messagemode1 by any member other than the team leader shall result in a warning Other team members must only use team message (messagemode2), while in a match Tournament Commitee may install third party program and/or join as an observer for tournament operations purposes, such as verifying match results or gathering match data.
