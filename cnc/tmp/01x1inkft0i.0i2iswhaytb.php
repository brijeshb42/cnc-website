							  		<h3>FIFA</h3>
							  		<pre>
General Rules

Players should report at least 5 minutes before the scheduled reporting time. If any engagements should be informed to the organized beforehand. If a player does not show up by the matchâ€™s reporting time, he/she will be disqualified.
Players may bring their own gear (Keyboard, Controller, Headphones, etc.). Though we would provide basic gear, joystick will not be provided.
Manhandling of gear or any rash behavior will result in disqualification from CNC 13.
Fixtures determined are final and binding. Requests regarding change of opponents will be on based on the discretion of the organizers.
Gameplay rules
Organizers will decide the player/team who creates the multiplayer game through a coin toss. This is because the player who creates the multiplayer game gets the first kick-off.
In case of conflicts of preferred teams, a toss will decide the team assigned to the players (It is henceforth advised that the second team provided by the player during registration is of the same or higher rating as the first preference team).
During a corner kick, players are not allowed to shoot the ball directly from the spot straight into goal. Players are only allowed to cross the ball into the 6 yard box if a short corner is done Any goals resulting from the above mentioned scenario shall be disallowed.
No Overhead through ball.
When the match ends in a draw extra time shall not be played till quarters. Match will be decided by penalties.
In case of interruption/connection lost match will be resumed from the current half. The score from the previous half will be carried on.
Game speed shall be normal or otherwise any if agreed upon by both players.
Initial stages will be 4 minutes per half. From quarter final onwards each half would be of 6 minutes.
Three substitutions will be allowed.
Time-play (time wastage) is forbidden.
It is forbidden to make a throw in directly into the opponent's penalty area. Should a player throw the ball directly into the opponent's penalty area he shall be warned. Should he persist, he shall be disqualified from the tournament. Any goals resulting from a throw in into the opponent's penalty area shall be disallowed.
A player/team who deliberately scores 2 own goals in a match will be given a warning. A subsequent (3rd) own-goal in the same match will result in immediate disqualification.
Decision in case of any dispute will be taken by the organizer and will be final and binding. No arguments will be entertained henceforth.
